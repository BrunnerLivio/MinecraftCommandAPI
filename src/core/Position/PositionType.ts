/**
 * @name PositionType
 * @description
 * A positiontype, which can be either Relative or Absolute
 */
export enum PositionType {
    /**
     * @name Relative
     * @description
     * A position, which is relative to another object
     */
    Relative,
    /**
     * @name Absolute
     * @description
     * A position, which is absolute
     */
    Absolute
}